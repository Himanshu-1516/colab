def ingest_pdf(pdf_path: str) -> BM25Encoder:
    """
    Load PDF → chunk → embed (dense + sparse) → upsert to Pinecone.
    Returns the fitted BM25Encoder needed at query time.
    """
    logger.info(f"Loading '{pdf_path}'...")
    loader    = PyPDFLoader(pdf_path)
    pages     = loader.load()
    logger.info(f"  Pages : {len(pages)}")

    splitter = RecursiveCharacterTextSplitter(
        chunk_size=CHUNK_SIZE,
        chunk_overlap=CHUNK_OVERLAP,
        length_function=len
    )
    chunks = splitter.split_documents(pages)
    logger.info(f"  Chunks: {len(chunks)}")

    texts = [c.page_content for c in chunks]

    # Fit BM25 on the full corpus
    bm25 = BM25Encoder()
    bm25.fit(texts)
    logger.info("  BM25 fitted.")

    # Build vectors and upsert in batches
    batch   = []
    total   = 0

    for i, (text, doc) in enumerate(zip(texts, chunks)):
        dense_vec     = embeddings.embed_query(text)
        sparse_result = bm25.encode_documents([text])[0]  # dict: {indices, values}

        batch.append({
            "id"    : f"chunk-{i}",
            "values": dense_vec,
            "sparse_values": {
                "indices": sparse_result["indices"],
                "values" : sparse_result["values"]
            },
            "metadata": {
                "text"    : text,                              # retrieved at query time
                "page"    : doc.metadata.get("page", 0),
                "chunk_id": i,
                "source"  : doc.metadata.get("source", pdf_path)
            }
        })

        if len(batch) >= UPSERT_BATCH_SIZE:
            main_index.upsert(vectors=batch)
            total += len(batch)
            logger.info(f"  Upserted {total}/{len(chunks)} chunks...")
            batch = []

    if batch:
        main_index.upsert(vectors=batch)
        total += len(batch)

    logger.info(f"  Done. Total upserted: {total} chunks.")
    logger.info(f"  Index stats: {main_index.describe_index_stats()}")
    return bm25

