def _wait_for_index(name: str) -> None:
    """Poll until the index reports ready status."""
    while True:
        status = pc.describe_index(name).status
        if status.get("ready", False):
            break
        logger.info(f"  Waiting for index '{name}' to be ready...")
        time.sleep(3)

