# 9. RERANKING  (cross-encoder on ORIGINAL query)
# ============================================================
def rerank_documents(
    original_query: str,
    docs: list[Document],
    top_k: int = TOP_K_RERANK
) -> list[Document]:
    """
    Score each doc against the ORIGINAL user query (not HyDE).
    HyDE is only used for retrieval recall — never for relevance scoring.
    """
    if not docs:
        return []

    pairs  = [[original_query, doc.page_content] for doc in docs]
    scores = reranker.predict(pairs)                        # numpy array

    ranked = sorted(zip(scores, docs), key=lambda x: x[0], reverse=True)

    result = []
    for score, doc in ranked[:top_k]:
        doc.metadata["rerank_score"] = float(score)
        result.append(doc)

    logger.info(
        f"  Reranked {len(docs)} → top {len(result)} "
        f"(best score: {result[0].metadata['rerank_score']:.4f})"
    )
    return result

