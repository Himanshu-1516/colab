def hyde(query):
    prompt = f"""Write a detailed passage that would likely appear in a document containing the answer to the user's question. Include important keywords, explain factually, and do not mention the question itself.\nQuestion:\n{query}\nPassage:\n"""
    return model.invoke(prompt).content

def construct_context(docs_with_metadata, top_k=3, score_threshold=0.0, max_tokens=2000):
    unique_docs = []
    for doc in docs_with_metadata:
        is_unique = True
        for u_doc in unique_docs:
            c1, c2 = set(doc['content'].lower().split()), set(u_doc['content'].lower().split())
            if not c1 or not c2: continue
            if len(c1 & c2) / len(c1 | c2) > 0.8:
                is_unique = False
                if doc.get('score', 0) > u_doc.get('score', 0):
                    unique_docs.remove(u_doc)
                    unique_docs.append(doc)
                break
        if is_unique: unique_docs.append(doc)

    source_counts = {}
    balanced_docs = []
    for doc in unique_docs:
        src = doc.get('source', 'unknown')
        if source_counts.get(src, 0) < 2:
            balanced_docs.append(doc)
            source_counts[src] = source_counts.get(src, 0) + 1

    top_docs = balanced_docs[:top_k]
    sorted_docs = sorted(top_docs, key=lambda x: (x['page'], x['chunk_id']))

    formatted_context = []
    for i, doc in enumerate(sorted_docs, 1):
        formatted_context.append(f"[Document {i} | Page {doc['page']} | Source: {doc.get('source', 'unknown')}]\n{doc['content']}")

    return "\n\n---\n\n".join(formatted_context), sorted_docs

def generate_answer(query, constructed_context):
    prompt = f"""You are an expert retrieval-augmented AI assistant. Answer using ONLY the provided context.\nQuestion:\n{query}\nContext:\n{constructed_context}\nAnswer:\n"""
    return model.invoke(prompt).content


def run_core_rag_pipeline(query: str) -> str:
    print("⏳ Running Full RAG Pipeline...")
    hypothetical_doc = hyde(query)
    retrieved_docs = retriever.invoke(hypothetical_doc)

    doc_texts = [doc.page_content for doc in retrieved_docs]
    pairs = [[hypothetical_doc, text] for text in doc_texts]
    scores = reranker.predict(pairs)
    ranked = sorted(zip(scores, retrieved_docs), key=lambda x: x[0], reverse=True)

    top_docs_for_context = []
    for score, doc in ranked[:5]:
        doc.metadata['score'] = float(score)
        top_docs_for_context.append({
            'content': doc.page_content,
            'page': doc.metadata.get('page', 0),
            'chunk_id': doc.metadata.get('chunk_id', 0),
            'source': doc.metadata.get('source', 'unknown'),
            'score': float(score)
        })

    constructed_context, used_docs = construct_context(top_docs_for_context, top_k=3)
    final_answer = generate_answer(query, constructed_context)
    return final_answer


