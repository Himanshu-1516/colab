# 10. CONTEXT CONSTRUCTION
# ============================================================
def _jaccard(a: str, b: str) -> float:
    sa, sb = set(a.lower().split()), set(b.lower().split())
    if not sa or not sb:
        return 0.0
    return len(sa & sb) / len(sa | sb)

def _deduplicate(docs: list[dict], threshold: float = 0.85) -> list[dict]:
    """Remove near-duplicate chunks; keep the one with the higher rerank score."""
    unique: list[dict] = []
    for doc in docs:
        duplicate = False
        for u in unique:
            if _jaccard(doc["content"], u["content"]) > threshold:
                if doc["rerank_score"] > u["rerank_score"]:
                    unique.remove(u)
                    unique.append(doc)
                duplicate = True
                break
        if not duplicate:
            unique.append(doc)
    return unique

def construct_context(
    docs: list[Document],
    top_k: int = TOP_K_CONTEXT,
    max_tokens: int = MAX_CONTEXT_TOKENS
) -> tuple[str, list[dict]]:
    """
    Pipeline:
      1. Convert docs to dicts
      2. Deduplicate (Jaccard)
      3. Trim to top_k
      4. Sort by document position (page → chunk)
      5. Truncate each chunk to stay within token budget
      6. Format into a readable string

    Returns (context_string, list_of_doc_dicts).
    """
    MAX_CHARS     = max_tokens * 4
    CHARS_PER_DOC = MAX_CHARS // max(top_k, 1)

    doc_dicts = [
        {
            "content"     : doc.page_content.strip(),
            "page"        : doc.metadata.get("page", 0),
            "chunk_id"    : doc.metadata.get("chunk_id", 0),
            "source"      : doc.metadata.get("source", "unknown"),
            "rerank_score": doc.metadata.get("rerank_score", 0.0),
            "hybrid_score": doc.metadata.get("hybrid_score", 0.0)
        }
        for doc in docs
    ]

    doc_dicts = _deduplicate(doc_dicts, threshold=0.85)
    doc_dicts = doc_dicts[:top_k]

    # Sort by reading order
    doc_dicts.sort(key=lambda x: (x["page"], x["chunk_id"]))

    # Truncate to budget
    final, total_chars = [], 0
    for doc in doc_dicts:
        content = doc["content"]
        if len(content) > CHARS_PER_DOC:
            content = content[:CHARS_PER_DOC]
            last_dot = content.rfind(".")
            if last_dot > CHARS_PER_DOC * 0.5:
                content = content[: last_dot + 1]
            content += " [truncated]"

        if total_chars + len(content) > MAX_CHARS:
            logger.info("  Context budget reached — stopping early.")
            break

        doc["content"] = content
        final.append(doc)
        total_chars += len(content)

    # Format
    sections = []
    for i, doc in enumerate(final, 1):
        sections.append(
            f"[Chunk {i} | Page {doc['page']} | "
            f"Rerank: {doc['rerank_score']:.4f} | "
            f"Source: {doc['source']}]\n"
            f"{doc['content']}"
        )

    context_str = "\n\n---\n\n".join(sections)
    logger.info(
        f"  Context: {len(final)} chunks, "
        f"~{total_chars} chars (~{total_chars // 4} tokens)."
    )
    return context_str, final

# ============================================================
# 11. ANSWER GENERATION
# ============================================================
_ANSWER_PROMPT = """\
You are an expert AI assistant. Answer the user's question using ONLY \
the information in the retrieved context below.

Rules:
- Be concise and factually precise.
- Cite chunk numbers when referencing specific facts, e.g. [Chunk 2].
- If the context does not contain sufficient information, respond with:
  "I could not find a complete answer in the provided documents."
- Never fabricate or infer information not present in the context.

Question:
{query}

Retrieved Context:
{context}

Answer:"""

def generate_answer(query: str, context: str) -> str:
    response = llm.invoke(_ANSWER_PROMPT.format(query=query, context=context))
    return response.content.strip()

# ============================================================
# 12. FULL RAG PIPELINE
# ============================================================
def run_rag_pipeline(query: str, bm25: BM25Encoder) -> str:
    """
    HyDE → Hybrid Retrieve → Rerank (original query) → Context → Answer
    """
    logger.info("─── RAG PIPELINE START ───")

    # 1. Generate hypothetical document (expands vocab for retrieval)
    hyp_doc = generate_hypothetical_doc(query)

    # 2. Hybrid retrieve using HyDE doc (better recall)
    retrieved = hybrid_retrieve(hyp_doc, bm25, top_k=TOP_K_RETRIEVE, alpha=HYBRID_ALPHA)

    if not retrieved:
        logger.warning("No documents retrieved from Pinecone.")
        return "I could not find relevant information in the uploaded document."

    # 3. Rerank using ORIGINAL query (accurate relevance scoring)
    reranked = rerank_documents(query, retrieved, top_k=TOP_K_RERANK)

    # 4. Build context string
    context_str, _ = construct_context(reranked, top_k=TOP_K_CONTEXT, max_tokens=MAX_CONTEXT_TOKENS)

    # 5. Generate grounded answer
    answer = generate_answer(query, context_str)

    logger.info("─── RAG PIPELINE END ───")
    return answer

# ============================================================
# 13. SEMANTIC CACHE  (lookup & store)
# ============================================================
def cache_lookup(query: str) -> Optional[str]:
    """
    Return cached answer if a sufficiently similar query exists.
    With normalize_embeddings=True + cosine index, score ∈ [0, 1].
    Higher score = more similar.
    """
    try:
        results = semantic_cache_store.similarity_search_with_score(query, k=1)
        if not results:
            logger.info("  Cache: empty.")
            return None

        matched_doc, score = results[0]
        logger.info(f"  Cache score: {score:.4f}  (threshold: {CACHE_SCORE_THRESHOLD})")

        if score >= CACHE_SCORE_THRESHOLD:
            answer = matched_doc.metadata.get("answer", "")
            if answer:
                logger.info("  CACHE HIT ✅")
                return answer

        logger.info("  CACHE MISS ❌")
        return None

    except Exception as e:
        logger.error(f"Cache lookup error: {e}")
        return None  # Fail open: proceed to full pipeline

def cache_store(query: str, answer: str) -> None:
    """Persist query-answer pair to the semantic cache index."""
    try:
        semantic_cache_store.add_texts(
            texts=[query],
            metadatas=[{
                "answer"    : answer,
                "query_text": query,
                "timestamp" : time.time()
            }]
        )
        logger.info("  Answer stored in cache ✅")
    except Exception as e:
        logger.error(f"Cache store error: {e}")

# ============================================================
# 14. CONDENSE QUESTION
# ============================================================
def condense_question(chat_history: list, latest_question: str) -> str:
    """Rephrase latest_question into a standalone question using history."""
    if not chat_history:
        return latest_question
    try:
        result = condense_chain.invoke({
            "chat_history"   : chat_history,
            "latest_question": latest_question
        })
        standalone = result.strip()
        logger.info(f"  Condensed → '{standalone}'")
        return standalone
    except Exception as e:
        logger.error(f"Condense chain error: {e}. Using original question.")
        return latest_question

# ============================================================
# 15. MAIN CHAT FUNCTION
# ============================================================
def chat(user_message: str, chat_history: list, bm25: BM25Encoder) -> str:
    """
    Single-turn entry point.
    1. Condense with history
    2. Cache lookup
    3. Full RAG if miss
    4. Cache store
    5. Return answer
    """
    print("\n" + "─" * 55)

    standalone = condense_question(chat_history, user_message)

    # Cache check
    cached = cache_lookup(standalone)
    if cached:
        print(f"⚡ [CACHE HIT] (threshold ≥ {CACHE_SCORE_THRESHOLD})")
        return cached

    # Full pipeline
    answer = run_rag_pipeline(standalone, bm25)

    # Persist to cache
    cache_store(standalone, answer)

    return answer

