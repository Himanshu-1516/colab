# 7. HYBRID RETRIEVAL  (dense + sparse via Pinecone query API)
# ============================================================
def hybrid_retrieve(
    query: str,
    bm25: BM25Encoder,
    top_k: int = TOP_K_RETRIEVE,
    alpha: float = HYBRID_ALPHA
) -> list[Document]:
    """
    alpha = 1.0  →  pure dense
    alpha = 0.0  →  pure sparse (BM25)
    alpha = 0.5  →  balanced hybrid  ← default
    """
    # Dense embedding
    dense_vec = embeddings.embed_query(query)

    # Sparse BM25 encoding  (encode_queries for query-time, not encode_documents)
    sparse_raw = bm25.encode_queries([query])[0]
    sparse_vec = {
        "indices": sparse_raw["indices"],
        "values" : sparse_raw["values"]
    }

    # Scale both sides
    scaled_dense  = [v * alpha       for v in dense_vec]
    scaled_sparse = {
        "indices": sparse_vec["indices"],
        "values" : [v * (1 - alpha) for v in sparse_vec["values"]]
    }

    response = main_index.query(
        vector=scaled_dense,
        sparse_vector=scaled_sparse,
        top_k=top_k,
        include_metadata=True
    )

    docs = []
    for match in response.matches:
        meta = match.metadata or {}
        docs.append(Document(
            page_content=meta.get("text", ""),
            metadata={
                "page"        : meta.get("page", 0),
                "chunk_id"    : meta.get("chunk_id", 0),
                "source"      : meta.get("source", "unknown"),
                "hybrid_score": round(match.score, 6)
            }
        ))

    logger.info(f"  Hybrid search returned {len(docs)} docs.")
    return docs

