import numpy as np
from sentence_transformers import CrossEncoder

def construct_context(retrieved_docs, top_k=3, score_threshold=0.0, max_tokens=2000):
    docs_with_metadata = []
    for doc in retrieved_docs:
        docs_with_metadata.append({
            'content': doc.page_content,
            'page': doc.metadata.get('page', 0),
            'chunk_id': doc.metadata.get('chunk_id', 0),
            'source': doc.metadata.get('source', 'unknown'),
            'score': doc.metadata.get('score', 0.0)
        })

    def filter_by_score(docs, threshold=0.0):
        if threshold <= 0.0:
            return docs
        filtered = [doc for doc in docs if doc.get('score', 0.0) >= threshold]
        return filtered

    def deduplicate(docs, threshold=0.8):
        unique_docs = []
        for doc in docs:
            is_unique = True
            for unique_doc in unique_docs:
                content1 = set(doc['content'].lower().split())
                content2 = set(unique_doc['content'].lower().split())
                if len(content1) == 0 or len(content2) == 0:
                    continue
                overlap = len(content1 & content2) / len(content1 | content2)
                if overlap > threshold:
                    is_unique = False
                    if doc.get('score', 0) > unique_doc.get('score', 0):
                        unique_docs.remove(unique_doc)
                        unique_docs.append(doc)
                    break
            if is_unique:
                unique_docs.append(doc)
        return unique_docs

    def manage_context_window(docs, max_tokens=2000):
        MAX_CHARS = max_tokens * 4
        CHUNK_MAX_CHARS = MAX_CHARS // max(len(docs), 1)
        managed_docs = []
        total_chars = 0
        for doc in docs:
            content = doc['content'].strip()
            if len(content) > CHUNK_MAX_CHARS:
                content = content[:CHUNK_MAX_CHARS]
                last_period = content.rfind('.')
                if last_period > CHUNK_MAX_CHARS * 0.5:
                    content = content[:last_period + 1]
                content = content + " [truncated]"
            if total_chars + len(content) > MAX_CHARS:
                break
            doc_copy = doc.copy()
            doc_copy['content'] = content
            managed_docs.append(doc_copy)
            total_chars += len(content)
        return managed_docs

    def balance_sources(docs, max_per_source=2):
        source_counts = {}
        balanced_docs = []
        for doc in docs:
            source = doc.get('source', 'unknown')
            count = source_counts.get(source, 0)
            if count < max_per_source:
                balanced_docs.append(doc)
                source_counts[source] = count + 1
        return balanced_docs

    def sort_by_position(docs):
        return sorted(docs, key=lambda x: (x['page'], x['chunk_id']))

    # Step 1: Score threshold filtering
    filtered_docs = filter_by_score(docs_with_metadata, threshold=score_threshold)
    # Step 2: Deduplicate
    unique_docs = deduplicate(filtered_docs, threshold=0.8)
    # Step 3: Source balancing
    balanced_docs = balance_sources(unique_docs, max_per_source=2)
    # Step 4: Take top K
    top_docs = balanced_docs[:top_k]
    # Step 5: Context window management
    managed_docs = manage_context_window(top_docs, max_tokens=max_tokens)
    # Step 6: Sort by position
    sorted_docs = sort_by_position(managed_docs)

    formatted_context = []
    for i, doc in enumerate(sorted_docs, 1):
        score_str = f"{doc['score']:.4f}" if doc.get('score', 0) > 0 else "N/A"
        source_str = doc.get('source', 'unknown')
        formatted_context.append(
            f"[Document {i} | Page {doc['page']} | Source: {source_str} | Score: {score_str}]\n{doc['content']}"
        )

    return "\n\n---\n\n".join(formatted_context), sorted_docs


