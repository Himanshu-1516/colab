class SemanticCache:
    def __init__(self, embeddings_model, threshold=0.85):
        self.embeddings = embeddings_model
        self.threshold = threshold
        self.dim = 384  # Dimension for all-MiniLM-L6-v2
        self.index = faiss.IndexFlatIP(self.dim) # Inner product (Cosine Similarity)
        self.cache_answers = []

    def _get_vector(self, text):
        vec = np.array([self.embeddings.embed_query(text)], dtype=np.float32)
        faiss.normalize_L2(vec)
        return vec

    def get_cached_answer(self, query):
        if self.index.ntotal == 0:
            return None
        vec = self._get_vector(query)
        distances, indices = self.index.search(vec, 1)
        if distances[0][0] >= self.threshold:
            print(f"\n⚡ SEMANTIC CACHE HIT! Similarity: {distances[0][0]:.4f}")
            return self.cache_answers[indices[0][0]]
        return None

    def add_to_cache(self, query, answer):
        vec = self._get_vector(query)
        self.index.add(vec)
        self.cache_answers.append(answer)

# Initialize Cache
semantic_cache = SemanticCache(embeddings)

