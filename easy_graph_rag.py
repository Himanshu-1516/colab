import networkx as nx

class KnowledgeGraphRAG:
    def __init__(self, llm):
        self.llm = llm
        self.graph = nx.Graph()

    def build_graph(self, documents):
        print("\n🕸️ Building Knowledge Graph from top retrieved contexts...")
        # Strict prompt to prevent hallucination
        prompt = """
        Extract knowledge triplets from the following text.
        Format exactly as: Entity1 | Relationship | Entity2
        RULES:
        - Only extract factual information EXPLICITLY present in the text.
        - Do not hallucinate or infer outside knowledge.
        - If no clear relationships exist, output 'NONE'.
        Text: {text}
        """
        for doc in documents:
            try:
                res = self.llm.invoke(prompt.format(text=doc.page_content)).content
                for line in res.split('\n'):
                    if '|' in line:
                        parts = [p.strip() for p in line.split('|')]
                        if len(parts) == 3:
                            self.graph.add_edge(parts[0], parts[2], relation=parts[1])
            except Exception as e:
                continue
        print(f"✅ Graph built! Nodes: {self.graph.number_of_nodes()} | Edges: {self.graph.number_of_edges()}")

    def get_graph_context(self, query):
        if self.graph.number_of_nodes() == 0:
            return ""

        # Extract entities from the query
        extract_prompt = f"Extract the main keywords/entities from this query as a comma-separated list. Query: {query}"
        try:
            entities = self.llm.invoke(extract_prompt).content.split(',')
            entities = [e.strip().lower() for e in entities]
        except:
            return ""

        context_relations = []
        for ent in entities:
            for node in self.graph.nodes():
                if ent in str(node).lower() or str(node).lower() in ent:
                    neighbors = self.graph.edges(node, data=True)
                    for u, v, data in neighbors:
                        context_relations.append(f"- {u} ({data.get('relation', 'related to')}) {v}")

        context_relations = list(set(context_relations)) # Deduplicate
        if not context_relations:
            return ""

        return "==== KNOWLEDGE GRAPH RELATIONSHIPS ====\n" + "\n".join(context_relations[:15])

