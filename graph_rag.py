class KnowledgeGraph:
    def __init__(self):
        self.graph: nx.DiGraph = nx.DiGraph()
        self.chunk_store: dict[str, Document] = {}
        self.entity_index: dict[str, set] = defaultdict(set)

    def _extract_entities(self, text: str) -> list[str]:
        doc = nlp(text[:1_000_000])
        entities = set()
        for ent in doc.ents:
            if ent.label_ in {"PERSON", "ORG", "GPE", "LOC", "PRODUCT", "EVENT", "WORK_OF_ART", "LAW", "NORP", "FAC"}:
                cleaned = ent.text.strip().lower()
                if len(cleaned) > 2:
                    entities.add(cleaned)
        for chunk in doc.noun_chunks:
            cleaned = chunk.root.lemma_.lower().strip()
            if len(cleaned) > 3 and chunk.root.pos_ in {"NOUN", "PROPN"} and not chunk.root.is_stop:
                entities.add(cleaned)
        return list(entities)

    def _extract_relations_llm(self, text: str, entities: list[str], chunk_id: str) -> list[tuple[str, str, str]]:
        if len(entities) < 2: return []
        entity_list = ", ".join(entities[:15])
        prompt = (
            "Extract factual relationships from the text below.\n"
            "Output ONLY valid JSON — a list of objects with keys: \"head\", \"relation\", \"tail\".\n"
            f"Use only entities from this list: {entity_list}\n"
            "If no clear relationships exist, return [].\n\n"
            f"Text:\n{text[:800]}\n\nJSON:"
        )
        try:
            response = llm.invoke(prompt)
            raw = response.content.strip()
            match = re.search(r"\[.*?\]", raw, re.DOTALL)
            if not match: return []
            triples = json.loads(match.group())
            valid = []
            for t in triples:
                h = str(t.get("head", "")).strip().lower()
                r = str(t.get("relation", "")).strip().lower()
                tl= str(t.get("tail", "")).strip().lower()
                if h and r and tl and h != tl: valid.append((h, r, tl))
            return valid
        except Exception as e:
            return []

    def add_chunk(self, chunk_id: str, text: str, doc: Document, use_llm_relations: bool = True) -> None:
        self.chunk_store[chunk_id] = doc
        entities = self._extract_entities(text)

        for ent in entities:
            if not self.graph.has_node(ent): self.graph.add_node(ent, chunks=set(), frequency=0)
            self.graph.nodes[ent]["chunks"].add(chunk_id)
            self.graph.nodes[ent]["frequency"] += 1
            self.entity_index[ent].add(chunk_id)

        for i, e1 in enumerate(entities):
            for e2 in entities[i + 1 :]:
                if e1 == e2: continue
                for src, dst in [(e1, e2), (e2, e1)]:
                    if self.graph.has_edge(src, dst):
                        self.graph[src][dst]["weight"] += 0.1
                        self.graph[src][dst]["chunks"].add(chunk_id)
                    else:
                        self.graph.add_edge(src, dst, weight=0.1, relation="co-occurs-with", chunks={chunk_id})

        if use_llm_relations:
            triples = self._extract_relations_llm(text, entities, chunk_id)
            for head, relation, tail in triples:
                for node in [head, tail]:
                    if not self.graph.has_node(node): self.graph.add_node(node, chunks=set(), frequency=0)
                    self.graph.nodes[node]["chunks"].add(chunk_id)
                    self.entity_index[node].add(chunk_id)

                if self.graph.has_edge(head, tail):
                    self.graph[head][tail]["weight"] = min(self.graph[head][tail]["weight"] + 0.5, 5.0)
                    self.graph[head][tail]["chunks"].add(chunk_id)
                    if relation != "co-occurs-with": self.graph[head][tail]["relation"] = relation
                else:
                    self.graph.add_edge(head, tail, weight=0.5, relation=relation, chunks={chunk_id})

    def retrieve_graph_context(self, query: str, top_k: int = GRAPH_TOP_K, hop_depth: int = GRAPH_HOP_DEPTH, weight_threshold: float = GRAPH_WEIGHT_THRESHOLD) -> list[Document]:
        if self.graph.number_of_nodes() == 0: return []

        query_entities = self._extract_entities(query)
        if not query_entities: return []

        seed_nodes = set()
        graph_nodes = set(self.graph.nodes())
        for qe in query_entities:
            if qe in graph_nodes: seed_nodes.add(qe)
            else:
                for gn in graph_nodes:
                    if qe in gn or gn in qe: seed_nodes.add(gn)

        if not seed_nodes: return []

        visited_nodes, collected_chunks, frontier = set(), set(), set(seed_nodes)

        for hop in range(hop_depth):
            next_frontier = set()
            for node in frontier:
                if node in visited_nodes: continue
                visited_nodes.add(node)
                collected_chunks.update(self.graph.nodes[node].get("chunks", set()))

                for neighbour in self.graph.successors(node):
                    edge_data = self.graph[node][neighbour]
                    if edge_data.get("weight", 0) >= weight_threshold and neighbour not in visited_nodes:
                        next_frontier.add(neighbour)
                        collected_chunks.update(edge_data.get("chunks", set()))

            frontier = next_frontier
            if not frontier: break

        chunk_scores = defaultdict(float)
        for node in visited_nodes:
            node_freq = self.graph.nodes[node].get("frequency", 1)
            for chunk_id in self.graph.nodes[node].get("chunks", set()):
                if chunk_id in collected_chunks: chunk_scores[chunk_id] += node_freq

        ranked_chunks = sorted(chunk_scores.items(), key=lambda x: x[1], reverse=True)[:top_k]
        result_docs = []
        for chunk_id, score in ranked_chunks:
            if chunk_id in self.chunk_store:
                doc = self.chunk_store[chunk_id]
                doc.metadata["graph_score"] = round(score, 4)
                doc.metadata["graph_hops"]  = hop_depth
                result_docs.append(doc)

        return result_docs

    def stats(self) -> dict:
        return {
            "nodes": self.graph.number_of_nodes(),
            "edges": self.graph.number_of_edges(),
            "chunks_indexed": len(self.chunk_store),
            "unique_entities": len(self.entity_index),
            "avg_degree": (sum(d for _, d in self.graph.degree()) / max(self.graph.number_of_nodes(), 1))
        }

    def visualize(self, max_nodes: int = 50, output_html: str = "knowledge_graph.html") -> None:
        if self.graph.number_of_nodes() == 0: return
        top_nodes = sorted(self.graph.degree(), key=lambda x: x[1], reverse=True)[:max_nodes]
        sub = self.graph.subgraph({n for n, _ in top_nodes})
        net = Network(height="600px", width="100%", directed=True, notebook=True, cdn_resources="in_line")
        for node, data in sub.nodes(data=True):
            freq = data.get("frequency", 1)
            net.add_node(node, label=node, size=min(10 + freq * 3, 40), title=f"Entity: {node}\nFrequency: {freq}\nChunks: {len(data.get('chunks', []))}")
        for src, dst, data in sub.edges(data=True):
            net.add_edge(src, dst, title=data.get("relation", "related"), value=data.get("weight", 0.1))
        net.set_options('{"physics": {"barnesHut": {"gravitationalConstant": -8000, "springLength": 150}, "minVelocity": 0.75}}')
        net.save_graph(output_html)
        display(HTML(output_html))

