def ingest_pdf(pdf_path: str, use_llm_relations: bool = True) -> BM25Encoder:
    logger.info(f"Loading '{pdf_path}'...")
    pages = PyPDFLoader(pdf_path).load()
    chunks = RecursiveCharacterTextSplitter(chunk_size=CHUNK_SIZE, chunk_overlap=CHUNK_OVERLAP, length_function=len).split_documents(pages)

    texts = [c.page_content for c in chunks]
    bm25 = BM25Encoder()
    bm25.fit(texts)

    batch, total = [], 0
    print(f"\n📊 Building knowledge graph from {len(chunks)} chunks...")

    for i, (text, doc) in enumerate(zip(texts, chunks)):
        chunk_id = f"chunk-{i}"
        dense_vec = embeddings.embed_query(text)
        sparse_result = bm25.encode_documents([text])[0]

        batch.append({
            "id": chunk_id, "values": dense_vec,
            "sparse_values": {"indices": sparse_result["indices"], "values": sparse_result["values"]},
            "metadata": {"text": text, "page": doc.metadata.get("page", 0), "chunk_id": i, "source": doc.metadata.get("source", pdf_path)}
        })

        if len(batch) >= UPSERT_BATCH_SIZE:
            main_index.upsert(vectors=batch)
            total += len(batch)
            batch = []

        graph_doc = Document(page_content=text, metadata={"page": doc.metadata.get("page", 0), "chunk_id": i, "source": doc.metadata.get("source", pdf_path)})
        knowledge_graph.add_chunk(chunk_id=chunk_id, text=text, doc=graph_doc, use_llm_relations=(use_llm_relations and i < 30))

    if batch:
        main_index.upsert(vectors=batch)
        total += len(batch)

    return bm25

