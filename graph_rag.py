# 8. HYBRID & GRAPH RETRIEVAL
# ============================================================
def hybrid_retrieve(query: str, bm25: BM25Encoder, top_k: int = TOP_K_RETRIEVE, alpha: float = HYBRID_ALPHA) -> list[Document]:
    dense_vec = embeddings.embed_query(query)
    sparse_raw = bm25.encode_queries([query])[0]
    response = main_index.query(
        vector=[v * alpha for v in dense_vec],
        sparse_vector={"indices": sparse_raw["indices"], "values": [v * (1 - alpha) for v in sparse_raw["values"]]},
        top_k=top_k, include_metadata=True
    )
    return [Document(page_content=m.metadata.get("text", ""), metadata={"page": m.metadata.get("page", 0), "chunk_id": m.metadata.get("chunk_id", 0), "source": m.metadata.get("source", "unknown"), "hybrid_score": round(m.score, 6), "retrieval": "hybrid"}) for m in response.matches]

def graph_aware_retrieve(original_query: str, hyp_doc: str, bm25: BM25Encoder) -> list[Document]:
    vector_docs = hybrid_retrieve(hyp_doc, bm25, top_k=TOP_K_RETRIEVE, alpha=HYBRID_ALPHA)
    graph_docs = knowledge_graph.retrieve_graph_context(original_query, top_k=GRAPH_TOP_K, hop_depth=GRAPH_HOP_DEPTH, weight_threshold=GRAPH_WEIGHT_THRESHOLD)

    merged = {}
    for doc in vector_docs[:int(TOP_K_RETRIEVE * (1 - GRAPH_RAG_BLEND))]:
        merged[str(doc.metadata["chunk_id"])] = doc

    for doc in graph_docs[:(TOP_K_RETRIEVE - len(merged))]:
        cid = str(doc.metadata["chunk_id"])
        if cid not in merged:
            doc.metadata["retrieval"] = "graph"
            merged[cid] = doc
        else:
            merged[cid].metadata["retrieval"] = "hybrid+graph"
            merged[cid].metadata["hybrid_score"] = merged[cid].metadata.get("hybrid_score", 0) * 1.1

    return list(merged.values())

def rerank_documents(original_query: str, docs: list[Document]) -> list[Document]:
    if not docs: return []
    scores = reranker.predict([[original_query, doc.page_content] for doc in docs])
    ranked = sorted(zip(scores, docs), key=lambda x: x[0], reverse=True)
    for score, doc in ranked[:TOP_K_RERANK]: doc.metadata["rerank_score"] = float(score)
    return [doc for _, doc in ranked[:TOP_K_RERANK]]

def construct_context(docs: list[Document]) -> str:
    sections = []
    for i, doc in enumerate(docs[:TOP_K_CONTEXT], 1):
        sections.append(f"[Chunk {i} | Page {doc.metadata.get('page', 0)} | Via {doc.metadata.get('retrieval', '')}]\n{doc.page_content[:MAX_CONTEXT_TOKENS*2]}")
    return "\n\n---\n\n".join(sections)

def generate_answer(query: str, context: str) -> str:
    prompt = f"Answer the user's question concisely using ONLY the provided context.\nCite chunks like [Chunk 1].\n\nQuestion: {query}\n\nContext:\n{context}\n\nAnswer:"
    return llm.invoke(prompt).content.strip()

def run_rag_pipeline(query: str, bm25: BM25Encoder) -> str:
    hyp_doc = llm.invoke(f"Write a short, factual passage (3-5 sentences) that directly answers this question: {query}").content.strip()
    retrieved = graph_aware_retrieve(query, hyp_doc, bm25)
    if not retrieved: return "I could not find relevant information in the uploaded document."
    reranked = rerank_documents(query, retrieved)
    context_str = construct_context(reranked)
    return generate_answer(query, context_str)

